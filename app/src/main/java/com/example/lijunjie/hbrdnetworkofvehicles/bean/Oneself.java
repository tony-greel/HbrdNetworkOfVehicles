public class Oneself {

    private String oneselfSerial;   //用户标识
    private String oneselfName;  //用户名
    private String oneselfPhone;   //手机
    private String oneselfIdCard;    //身份证
    private String oneselfIdName;   //姓名

    public String getOneselfSerial() {
        return oneselfSerial;
    }

    public void setOneselfSerial(String oneselfSerial) {
        this.oneselfSerial = oneselfSerial;
    }

    public String getOneselfName() {
        return oneselfName;
    }

    public void setOneselfName(String oneselfName) {
        this.oneselfName = oneselfName;
    }

    public String getOneselfPhone() {
        return oneselfPhone;
    }

    public void setOneselfPhone(String oneselfPhone) {
        this.oneselfPhone = oneselfPhone;
    }

    public String getOneselfIdCard() {
        return oneselfIdCard;
    }

    public void setOneselfIdCard(String oneselfIdCard) {
        this.oneselfIdCard = oneselfIdCard;
    }

    public String getOneselfIdName() {
        return oneselfIdName;
    }

    public void setOneselfIdName(String oneselfIdName) {
        this.oneselfIdName = oneselfIdName;
    }
}
